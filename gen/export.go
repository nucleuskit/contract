package gen

import (
	"bytes"
	"fmt"
	"sort"
	"strings"

	"github.com/nucleuskit/contract/openapi"
)

// ExportDocs renders Markdown documentation from OpenAPI contract metadata.
func ExportDocs(dir string) ([]byte, error) {
	endpoints, err := openapi.LoadEndpoints(dir)
	if err != nil {
		return nil, err
	}
	schemas, err := openapi.LoadResolvedSchemas(dir)
	if err != nil {
		return nil, err
	}
	var buffer bytes.Buffer
	buffer.WriteString("# Nucleus Contract\n\n")
	buffer.WriteString("## Endpoints\n\n")
	for _, endpoint := range endpoints {
		fmt.Fprintf(&buffer, "- %s %s", endpoint.Method, endpoint.Path)
		if endpoint.OperationID != "" {
			fmt.Fprintf(&buffer, " (`%s`)", endpoint.OperationID)
		}
		buffer.WriteByte('\n')
	}
	if len(endpoints) == 0 {
		buffer.WriteString("- none\n")
	}
	buffer.WriteString("\n## Schemas\n\n")
	for _, name := range sortedSchemaNames(schemas) {
		schema := schemas[name]
		fmt.Fprintf(&buffer, "### %s\n\n", name)
		if schema.Description != "" {
			buffer.WriteString(schema.Description)
			buffer.WriteString("\n\n")
		}
		if len(schema.Properties) == 0 {
			fmt.Fprintf(&buffer, "- `%s`\n\n", schemaTypeName(schema))
			continue
		}
		required := requiredSet(schema)
		for _, propertyName := range sortedPropertyNames(schema.Properties) {
			property := schema.Properties[propertyName]
			line := fmt.Sprintf("- `%s` %s", propertyName, schemaTypeName(property))
			if required[propertyName] {
				line += " required"
			} else {
				line += " optional"
			}
			if property.Description != "" {
				line += " - " + property.Description
			}
			buffer.WriteString(line)
			buffer.WriteByte('\n')
		}
		buffer.WriteByte('\n')
	}
	if len(schemas) == 0 {
		buffer.WriteString("- none\n")
	}
	return buffer.Bytes(), nil
}

// ExportTypeScript renders TypeScript declarations from OpenAPI schemas.
func ExportTypeScript(dir string) ([]byte, error) {
	schemas, err := openapi.LoadResolvedSchemas(dir)
	if err != nil {
		return nil, err
	}
	var buffer bytes.Buffer
	buffer.WriteString("// Generated by nucleus gen --typescript. Do not edit.\n\n")
	for _, name := range sortedSchemaNames(schemas) {
		writeTypeScriptSchema(&buffer, name, schemas[name])
		buffer.WriteByte('\n')
	}
	return buffer.Bytes(), nil
}

// ExportClientBundle renders client artifacts for the requested languages.
//
// If languages is empty, TypeScript, Dart, Java, and Kotlin clients are returned.
func ExportClientBundle(dir string, languages []string) (map[string][]byte, error) {
	routes, err := openapi.LoadRouteRegistry(dir)
	if err != nil {
		return nil, err
	}
	if len(languages) == 0 {
		languages = []string{"typescript", "dart", "java", "kotlin"}
	}
	clients := make(map[string][]byte, len(languages))
	for _, language := range languages {
		language = normalizeClientLanguage(language)
		if language == "" {
			continue
		}
		client, err := ExportClient(routes, language)
		if err != nil {
			return nil, err
		}
		clients[language] = client
	}
	return clients, nil
}

// ExportClient renders a client artifact for one supported language.
func ExportClient(routes []openapi.Route, language string) ([]byte, error) {
	if err := validateUniqueClientOperationNames(routes); err != nil {
		return nil, err
	}
	switch normalizeClientLanguage(language) {
	case "typescript":
		return exportTypeScriptClient(routes), nil
	case "dart":
		return exportDartClient(routes), nil
	case "java":
		return exportJavaClient(routes), nil
	case "kotlin":
		return exportKotlinClient(routes), nil
	default:
		return nil, fmt.Errorf("unsupported client language %q", language)
	}
}

func validateUniqueClientOperationNames(routes []openapi.Route) error {
	seen := map[string]openapi.Route{}
	for _, route := range routes {
		name := clientOperationName(route)
		if previous, ok := seen[name]; ok {
			return fmt.Errorf("duplicate generated client operation name %q for %s and %s", name, clientOperationLabel(previous), clientOperationLabel(route))
		}
		seen[name] = route
	}
	return nil
}

func clientOperationLabel(route openapi.Route) string {
	if strings.TrimSpace(route.OperationID) != "" {
		return route.OperationID
	}
	return strings.TrimSpace(route.Method + " " + route.Path)
}

func exportTypeScriptClient(routes []openapi.Route) []byte {
	var buffer bytes.Buffer
	buffer.WriteString("// Generated by nucleus gen --clients. Do not edit.\n\n")
	buffer.WriteString("export interface HttpTransport {\n  request(method: string, path: string, body?: unknown): Promise<unknown>;\n}\n\n")
	buffer.WriteString("export class NucleusClient {\n  constructor(private readonly transport: HttpTransport) {}\n\n")
	for _, route := range routes {
		fmt.Fprintf(&buffer, "  %s(body?: unknown): Promise<unknown> {\n", clientOperationName(route))
		fmt.Fprintf(&buffer, "    return this.transport.request(%q, %q, body);\n", route.Method, route.Path)
		buffer.WriteString("  }\n\n")
	}
	buffer.WriteString("}\n")
	return buffer.Bytes()
}

func exportDartClient(routes []openapi.Route) []byte {
	var buffer bytes.Buffer
	buffer.WriteString("// Generated by nucleus gen --clients. Do not edit.\n\n")
	buffer.WriteString("abstract class NucleusTransport {\n  Future<Object?> request(String method, String path, [Object? body]);\n}\n\n")
	buffer.WriteString("class NucleusClient {\n  final NucleusTransport transport;\n  NucleusClient(this.transport);\n\n")
	for _, route := range routes {
		fmt.Fprintf(&buffer, "  Future<Object?> %s([Object? body]) {\n", clientOperationName(route))
		fmt.Fprintf(&buffer, "    return transport.request(%q, %q, body);\n", route.Method, route.Path)
		buffer.WriteString("  }\n\n")
	}
	buffer.WriteString("}\n")
	return buffer.Bytes()
}

func exportJavaClient(routes []openapi.Route) []byte {
	var buffer bytes.Buffer
	buffer.WriteString("// Generated by nucleus gen --clients. Do not edit.\n\n")
	buffer.WriteString("public final class NucleusClient {\n")
	buffer.WriteString("  public interface Transport { Object request(String method, String path, Object body) throws Exception; }\n")
	buffer.WriteString("  private final Transport transport;\n")
	buffer.WriteString("  public NucleusClient(Transport transport) { this.transport = transport; }\n\n")
	for _, route := range routes {
		fmt.Fprintf(&buffer, "  public Object %s(Object body) throws Exception {\n", clientOperationName(route))
		fmt.Fprintf(&buffer, "    return transport.request(%q, %q, body);\n", route.Method, route.Path)
		buffer.WriteString("  }\n\n")
	}
	buffer.WriteString("}\n")
	return buffer.Bytes()
}

func exportKotlinClient(routes []openapi.Route) []byte {
	var buffer bytes.Buffer
	buffer.WriteString("// Generated by nucleus gen --clients. Do not edit.\n\n")
	buffer.WriteString("fun interface NucleusTransport {\n  fun request(method: String, path: String, body: Any? = null): Any?\n}\n\n")
	buffer.WriteString("class NucleusClient(private val transport: NucleusTransport) {\n")
	for _, route := range routes {
		fmt.Fprintf(&buffer, "  fun %s(body: Any? = null): Any? {\n", clientOperationName(route))
		fmt.Fprintf(&buffer, "    return transport.request(%q, %q, body)\n", route.Method, route.Path)
		buffer.WriteString("  }\n\n")
	}
	buffer.WriteString("}\n")
	return buffer.Bytes()
}

func normalizeClientLanguage(language string) string {
	switch strings.ToLower(strings.TrimSpace(language)) {
	case "ts", "typescript":
		return "typescript"
	case "dart":
		return "dart"
	case "java":
		return "java"
	case "kt", "kotlin":
		return "kotlin"
	default:
		return strings.ToLower(strings.TrimSpace(language))
	}
}

func clientOperationName(route openapi.Route) string {
	name := strings.TrimSpace(route.OperationID)
	if name == "" {
		name = routeMethodName(route)
	}
	return lowerFirst(exportName(name))
}

func lowerFirst(name string) string {
	if name == "" {
		return "call"
	}
	bytes := []byte(name)
	if bytes[0] >= 'A' && bytes[0] <= 'Z' {
		bytes[0] += 'a' - 'A'
	}
	return string(bytes)
}

func writeTypeScriptSchema(buffer *bytes.Buffer, name string, schema openapi.Schema) {
	typeName := exportName(name)
	if schema.Type != "object" && len(schema.Properties) == 0 {
		fmt.Fprintf(buffer, "export type %s = %s;\n", typeName, typeScriptType(schema))
		return
	}
	fmt.Fprintf(buffer, "export interface %s {\n", typeName)
	required := requiredSet(schema)
	for _, propertyName := range sortedPropertyNames(schema.Properties) {
		property := schema.Properties[propertyName]
		optional := "?"
		if required[propertyName] {
			optional = ""
		}
		fmt.Fprintf(buffer, "  %s%s: %s;\n", typeScriptPropertyName(propertyName), optional, typeScriptType(property))
	}
	buffer.WriteString("}\n")
}

func typeScriptType(schema openapi.Schema) string {
	if len(schema.Enum) > 0 {
		values := make([]string, 0, len(schema.Enum))
		for _, value := range schema.Enum {
			values = append(values, fmt.Sprintf("%q", fmt.Sprint(value)))
		}
		sort.Strings(values)
		return strings.Join(values, " | ")
	}
	switch schema.Type {
	case "string":
		return "string"
	case "integer", "number":
		return "number"
	case "boolean":
		return "boolean"
	case "array":
		if schema.Items == nil {
			return "unknown[]"
		}
		return typeScriptType(*schema.Items) + "[]"
	case "object":
		if len(schema.Properties) == 0 {
			return "Record<string, unknown>"
		}
		parts := make([]string, 0, len(schema.Properties))
		required := requiredSet(schema)
		for _, propertyName := range sortedPropertyNames(schema.Properties) {
			optional := "?"
			if required[propertyName] {
				optional = ""
			}
			parts = append(parts, fmt.Sprintf("%s%s: %s", typeScriptPropertyName(propertyName), optional, typeScriptType(schema.Properties[propertyName])))
		}
		return "{ " + strings.Join(parts, "; ") + " }"
	default:
		return "unknown"
	}
}

func schemaTypeName(schema openapi.Schema) string {
	if schema.Type == "array" && schema.Items != nil {
		return schemaTypeName(*schema.Items) + "[]"
	}
	if schema.Type != "" {
		return schema.Type
	}
	return "unknown"
}

func sortedSchemaNames(schemas map[string]openapi.Schema) []string {
	names := make([]string, 0, len(schemas))
	for name := range schemas {
		names = append(names, name)
	}
	sort.Strings(names)
	return names
}

func sortedPropertyNames(properties map[string]openapi.Schema) []string {
	names := make([]string, 0, len(properties))
	for name := range properties {
		names = append(names, name)
	}
	sort.Strings(names)
	return names
}

func requiredSet(schema openapi.Schema) map[string]bool {
	required := map[string]bool{}
	for _, name := range schema.Required {
		required[name] = true
	}
	return required
}

func exportName(name string) string {
	name = strings.TrimSpace(name)
	if name == "" {
		return "Anonymous"
	}
	var builder strings.Builder
	capitalize := true
	for _, r := range name {
		if (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z') || (r >= '0' && r <= '9') {
			if capitalize && r >= 'a' && r <= 'z' {
				r -= 'a' - 'A'
			}
			builder.WriteRune(r)
			capitalize = false
			continue
		}
		capitalize = true
	}
	if builder.Len() == 0 {
		return "Anonymous"
	}
	return builder.String()
}

func typeScriptPropertyName(name string) string {
	if isIdentifier(name) {
		return name
	}
	return fmt.Sprintf("%q", name)
}

func isIdentifier(name string) bool {
	if name == "" {
		return false
	}
	for index, r := range name {
		if index == 0 {
			if (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z') || r == '_' {
				continue
			}
			return false
		}
		if (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z') || (r >= '0' && r <= '9') || r == '_' {
			continue
		}
		return false
	}
	return true
}
